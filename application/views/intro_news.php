            	<p class="daoyu">
                 　　日新网新闻出版中心下设编辑部、记者团，全面负责校园内外的新闻报道工作。新闻出版中心的全体成员始终坚持以崭新的姿态和进取的精神，用更加及时、全面、深入的报道赢得新的认可和肯定。
                </p>
                <br />
                <p class="title_b">编辑部</p>
                <p class="daoyu">
                　　编辑部主要负责日新网内容的更新维护、《交大青年》的编辑出版工作，设有总编辑一名，中文编辑两名，记者团团长一名、副团长一名，英
文编辑一名以及助理编辑若干名。日新新闻编辑部坚持服务师生、言论自由的宗旨，在维护并提升日新言论、日新
访谈等日新品牌栏目的内容与质量的同时，另外开辟专题新闻、专栏新闻等诸多新闻栏目，对校内各类事件给予广
泛的关注。
                </p>
                <p class="title_s">　中文编辑</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br /> 
    1、新闻敏感性强，在校级或校级以上媒体发表过优质文章，能适应中午及晚间工作；<br />
    2、有较好的计算机基础，熟练使用各种办公软件与一种图片处理软件（如photoshop等）；<br/>
    3、了解publisher、dreamweaver、电子杂志制作其中一种或多种技能；<br />
    4、有团队合作精神，并有较强的责任心和工作热情，执行能力强；<br />
    5、2010级、2011级不考研的在校学生。
                    </p>
                </div><br />
                <p class="title_s">　助理编辑</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br /> 
    1、新闻敏感性强，有较好的文字功底，在院级或院级以上媒体发表过优质文章；<br /> 
    2、有一定的计算机基础，熟练使用常规办公软件，了解图片处理；<br /> 
	3、有团队合作精神，较强的责任心和工作热情，并有一定的组织协调能力；<br /> 
    4、2010级、2011级不考研的在校学生。
                    </p>
                </div>
                <div class="bottom_border" style="width:100%; height:27px; border-bottom:dashed 1px #7e7e7e;"></div>
                <br />
                <!--记者-->
                 <p id="1" class="title_b">记者团</p>
                <p class="daoyu">
                　　记者团由原南北区记者站合并而成，主要负责校内外各类新闻的采写。目前，记者团已经发展成为拥有50余名
文字记者、摄影记者、体育记者的优秀记者团队，除完成日常校园新闻的报道外，在一些日新网组织、参与的校内
外重大活动中发挥着巨大的作用。记者团先后涌现出了"世博会注册大学生记者"、"世博会香港传播大使"、"
亚运会注册大学生记者"、"大运会注册大学生记者"、"城运会注册记者"等多名优秀校园新闻人。
                </p>
                <p class="title_s">　文字记者</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                     职位要求：<br />
    1、新闻敏感性强，需要有一定的文字功底；<br />
    2、有团队合作精神，并有较强的责任心和工作热情；<br />
    3、熟悉常规办公软件常用操作（如word等)。
                    </p>
                </div><br />
                <p class="title_s">　摄影记者</p>
                <div style="height:10px;"></div>
                <div class="txt_box">
                	<p class="daoyu">
                    职位要求：<br />
    1、新闻敏感性强，需要有一定的摄影基础以及摄影器材（有单反者优先）；<br />
    2、有团队合作精神，并有较强的责任心和工作热情；<br />
    3、了解图片处理技能。
