            	<p class="daoyu">
                 　　日新网行政管理中心是日新工作室部门之一，其前身为日新行政部。日新网行政管理中心的宗旨是打造交大最好的第二课堂社会实践平台。行政管理中心下设办公室、财务部、企划部、人力资源部、市场部、驻外办，共6个部门。中心设有行政总监，部门设有办公室主任、财务部经理、企划部经理、人力资源部经理、市场部经理、驻外办经理。行政管理中心对内主要负责网站内部的行政建设，运行日新部分网络产品及线下精品活动，做好网站的后勤服务、组织内部人员活动；对外则是与校内外各社团、学生会等其他学生组织合作，不定期与各高校兄弟网站之间开展交流、合作等活动，承担网站的部分外宣与外部交流工作。
                </p>
                <br />
                <p id="1" class="title_b">办公室</p>
                <p class="daoyu">
                　　办公室是日新网行政管理中心部门之一，办公室主要负责协助全站进行网站的行政建设与校内外单位的走访交流合作，以及日新网的线下精品活动栏目，同时负责学校南区礼堂周末影院的日常管理工作，此外，办公室负责对办公场所的使用情况进行登记归档，负责对来访人员的欢迎接待工作。
                </p>
