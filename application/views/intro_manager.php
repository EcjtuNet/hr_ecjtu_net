    3、具有较好的人际交往能力、沟通能力和应变能力;<br />
    4、做事认真，注重细节，责任心强，工作积极。<br />
    职位职责：<br />
    1、协助办公室主任处理日常工作；<br />
    2、负责办公场所的分配和使用；<br />
    3、负责办公室的日常值班；
                    </p>
