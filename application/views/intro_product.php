                <p class="daoyu">
                　　《交大青年》是日新网旗下杂志，于2011年3月由华东交通大学学工处、校团委、日新网联合发起筹备创办，目前发行量数千册。其具体定位为“综合类校园杂志”，旨在传播知识、交流观点、启迪心灵，树立华东交大之精神。
杂志目前由日新网新闻出版中心负责编辑出版，通常情况下杂志固定为两月出版一期，假期停刊。除固定出版的双月刊外，日新网新闻出版中心也会根据需要出版特刊（如交大40周年校庆专刊，建团九十周年特刊等）。
《交大青年》创办短短两年，但已覆盖整个交大校园。此外，通过与校外媒体间的交流，《交大青年》已名闻全国众多高校。

